export type RegionBase = {
  id: string,
  name: string,
  chi_name: string,
  type: string,
  population: number,
  food: number,
  metal: number,
  industry: number,
  wood: number,
  business: number,
}
export type REGION_BASE = {
  id: string,
  name: string,
  chi_name: string,
  type: string,
  population: string,
  food: string,
  metal: string,
  industry: string,
  wood: string,
  business: string,
}

function convertRegionFromRaw(region: REGION_BASE): RegionBase {
  return {
    ...region,
    population: parseInt(region.population, 10),
    food: parseInt(region.food, 10),
    metal: parseInt(region.metal, 10),
    industry: parseInt(region.industry, 10),
    wood: parseInt(region.wood, 10),
    business: parseInt(region.business, 10),
  };
}

